package br.com.suhmoraes.desafioorangetalents.client.model;

import com.fasterxml.jackson.annotation.JsonProperty;

public class CarYear {
	
	@JsonProperty("nome")
	private String name;
	
	@JsonProperty("codigo")
	private String code;

	public CarYear() {		
		this.name = "";
		this.code = "";
	}

	public String getName() {
		return name;
	}

	public String getCode() {
		return code;
	}

	
	
	

	
}
